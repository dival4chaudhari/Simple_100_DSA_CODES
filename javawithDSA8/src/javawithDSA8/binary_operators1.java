package javawithDSA8;

public class binary_operators1 {

	public static void main(String args[])
	{
		System.out.println(5 & 6); // binary "and" operator output = 4
		System.out.println(5 | 6); //binary "or" operator output = 7
		System.out.println(5 ^ 6); //binary "Xor" operator output = 3
		System.out.println(~5); //binary not operator output = -6
		System.out.println(~7); //binary not operator output = -6
		System.out.println(5<<2); //binary left shift operator output = 20
		System.out.println(9>>2);

	}
}
